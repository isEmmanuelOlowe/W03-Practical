class Feature {
  int number;
  String name;
  String[] style;
  double rating;
  String priceRange;
  double reviewNo;
  String urlTA;
  String idTA;

  public Feature(int number, String name, String style, double rating, String priceRange, String urlTA, String idTA){
    int number = this.number;
    String name = this.name;
    String[] style = this.style;
    double rating = this.rating;
    String priceRange = this.priceRange;
    double reviewNo = this.reviewNo;
    String urlTA = this.urlTA;
    String idTA = this.idTA;
  }
}
